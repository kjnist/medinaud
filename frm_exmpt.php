                <h2>
                    Concerning filing:
                </h2>
                <ol>
                    <li>
                        If there are currently special assessments on your parcel or special assessments assessed in the future, these are not eligible for exemption. Special assessments assessed to a parcel will generate a tax bill.
                    </li>
                    <li>
                        Commercial Agricultural Usage Value (CAUV) recoupage cannot be exempted unless the property owner is a political subdivision.
                    </li>
                    <li>
                        The Auditor's Office recommends that you submit one parcel per request. If you do list multiple parcels on the same request, they must be in the same school district. However, the entire process will be held up if there is a problem with one parcel.
                    </li>
                    <li>
                        We require three (3) complete exemption request forms with three (3) copies of any supporting documents. The requests should each have an original signature.
                    </li>
                    <li>
                        Please indicate a point-of-contact and phone number should there be any questions on the request. This may be someone other than the person signing the request.
                    </li>
                </ol>
                <h2>
                    Concerning taxes:
                </h2>
                <ol>
                    <li>
                        Your organization must be in title and three (3) copies of the last recorded deed for all parcels on the request must be provided at the time the request is submitted.
                    </li>
                    <li>
                        Prior to an exemption application being forwarded to the State Department of Taxation, taxes are required to be paid in full for the year in which title is acquired. Taxes paid for the year in which title is acquired cannot be refunded unless the request is for new construction on an exempt parcel.                    \
                    </li>
                    <li>
                        If your request is for new construction on a parcel currently exempt, taxes do not need to be paid when the exemption request is submitted. This will allow the State Department of Taxation to review the exemption application on the new construction. The new construction value will be put on the tax rolls and generate a tax bill until the State makes a determination.
                    </li>
                    <li>
                        If your parcel number changes from the originally exempted parcel, a new exemption must be submitted to the state.
                    </li>
                </ol>
                <p>
                    When the application is received at the Auditor's Office, it will be forwarded to the State within ten working days. Please call Erica Graffein (330) 725-9767 when you receive any correspondence from the State, or if you have any questions.
                </p>
                <p>
                    If you have not received a Final Determination from the State within 10 months, we need to be notified so we can determine what is holding up the process and facilitate its completion.
