            <div class="padding">
                <p>
                    Weights and Measures plays a vital role in our everyday lives. Our economy
                    is based on the monetary value placed on goods and services bought, sold and
                    traded daily.
                </p>
                <p>
                    According to Chapter 119 of the Revised Code, commercial weighing and 
                    measuring devices, such as scales and scanners, must be positioned so that its
                    indications may be accurately read and the weighing or measuring operation may
                    be observed from some reasonable "customer" position.
                </p>
                <p>
                    The Ohio Department of Agriculture is the custodian of the Ohio Primary of 
                    Weights and Measures, which are traceable to U.S. Standards of the National 
                    Institute of Standards and Technology (NIST) in Gaithersburg, Maryland. Local
                    jurisdictions are responsible for testing and inspecting all commercial 
                    weighing and measuring devices such as scales, gas pumps, UPC scanners and
                    more. The state assists in the testing and inspection of vehicle scales,
                    livestock scales, fuel meters (fuel oil, gasoline and LP gas) and packaged 
                    consumer goods.
                </p>
                <h2>
                    Scales:
                </h2>
                <p>
                    Commercial scales are used to price items by weight. A deli scale is one of 
                    the more frequently checked scales of this type.  One reason for checking the
                    products in a deli or grocery store is that the tare weight can be added to
                    the product weight which would make the overall price greater. 
                </p>  
                <p>
                    Non-commercial scales are the scales that you see in doctors' offices or 
                    schools. They are tested for accuracy against standard weight blocks.
                    Locally, when wrestling tournaments are held in the schools, the weigh-in 
                    scales will be tested.
                </p>
                <ol>
                    <li>
                        Watch the scale and the amount registered.  The scale should be placed so
                        you can see the weight, price and other information displayed.
                    </li>
                    <li>
                        Make sure the scale shows a zero or minus sign before anything is 
                        weighed.  Pay only for the product, not for the packaging.
                    </li>
                    <li>
                        If you have any questions about how a store weighs or measures products,
                        ask the manager for information first.  He or she should be able to 
                        answer your questions.
                    </li>
                    <li>
                        If the problem is not resolved, contact your Medina County Weights and 
                        Measures Department for assistance.
                    </li>
                </ol>
                <h2>
                    UPC Scanners:
                </h2>
                <p>
                    The purpose of checking UPC scanners is ensure they are working correctly.
                    Our Weights and Measures Inspectors randomly select items from a store, 
                    choosing items from all departments.  They write down the item and the 
                    sticker price, then take the items to the register and scan each item, to 
                    ensure the sticker price and checkout price coincide. Stores are required to
                    correct the pricing in their computer so that the item scans correctly.
                </p>
                <ol>
                    <li>
                        Watch as the price of the item appears on the check-out register.
                    </li>
                    <li>
                        Ask the clerk to check the price if you think the scanned price is incorrect.
                    </li>
                    <li>
                         If the scanned price does not agree with the posted price, ask the store manager to correct it.
                    </li>
                    <li>
                        Save your purchase receipt, in case you have questions or a problem later on.
                    </li>
                    <li>
                         If the problem is not resolved, contact your Medina County Weights and Measures Department for assistance.
                    </li>
                </ol>
                <h2>
                    Gas Cans:
                </h2>
                <P>
                    We often receive calls from residents who paid for more than two gallons 
                    worth of gas and wonder if they were cheated.  Usually the gas can is to 
                    blame, since they are generally inaccurate in measurement.  Gas cans are 
                    "approximates."  If you have a two-gallon can, it may actually take a little
                    more or a little less to fill the can up.
                </P>
                <h2>
                    Firewood:
                </h2>
                <p>
                    The Medina County Auditor's Office would like to remind people that, with 
                    the onset of cold weather, more people will be purchasing firewood.  Legally,
                    firewood should only be sold by the "cord" or by fractions of a cord.  A 
                    cord is 128 cubic feet, which is the height multiplied by the the length,
                    multiplied by the width.
                </p>
                <p>
                    It is important to know that terms such as "truckload," "face cord," "rack"
                    and "pile" have no legally-defined meaning and therefore a buyer has no way
                    of determining how much firewood they are actually receiving.  Make sure you
                    have a telephone number and sales receipt for your wood.  Stack your wood 
                    when you receive it and determine if it is a cord.  If not, and if the seller
                    will not correct the problem, contact the Weights and Measures Department
                    for assistance.
                </p>
