                <p>
                    Many Medina County agricultural landowners may be surprised when they open 
                    their 2014 tax bills. For those on Current Agricultural Use Values (CAUV) 
                    program, property values have increased several hundred percent over 2010 
                    CAUV values, depending on soil type. Ohio has approximately 3,500 soil types. 
                </p>
                <p>
                    Unlike fair market value appraisals, CAUV values are calculated by the State
                    of Ohio Department of Taxation by a formula that is based on five factors. 
                    The CAUV soil rates allow land to be taxed based on its agricultural value,
                    rather than fair market value, which is considerably higher. The county 
                    auditor has no control over the calculation performed by the state. The 
                    auditor's role in this regard is simply to apply the results of the 
                    predetermined calculation from the Ohio Department of Taxation. 
                </p>
                <p>
                    The five factors the Ohio Department of Taxation uses in calculating CAUV 
                    land values are yield information, cropping patterns, crop prices, non-land 
                    production costs and the capitalization rate. CAUV rates are updated every 
                    three years and current rates were updated because of the state mandated 
                    2013 sexennial reappraisal. Even with the significantly increased CAUV
                    values, farmers are paying taxes on values significantly less than those 
                    not on the CAUV program. In 2012 the statewide average value for CAUV 
                    acreage was $994, compared to $3,044 per acre for agricultural acreage 
                    not in the CAUV program. Because the county auditor has no role in setting
                    the CAUV values, there is no appeal available to land owners at the local 
                    level. 
                </p>
