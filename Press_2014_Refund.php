                <p>
                    MEDINA, Ohio (August 22, 2014) – Medina County Auditor Mike Kovack today
                    announced that the Medina County Auditor’s Office prudent and efficient 
                    use of taxpayer dollars has allowed him to return a record $5 million to
                    local schools, agencies and municipalities from the Real Estate Assessment
                    Fund.
                </p>
                <p>
                    The Real Estate Assessment Fund comes from a portion of property taxes
                    collected and set aside to pay for real estate appraisals. The fees are 
                    mandated by state law and are used to finance the reappraisal process, 
                    but auditors are allowed to refund the fees following reappraisal if they 
                    have completed the work for less than the amount. The surplus is redistributed
                    to local governments and schools in proportion to the amount they contributed
                    to the fund. The Medina County Auditor’s Office completed the State mandated
                    2013 Sexennial Reappraisal last fall.
                </p>
                <p>
                    Medina County schools will receive $3.61 million of the refund. County 
                    agencies will be given $747,000 while municipalities and townships will 
                    receive about $643,000.
                </p>
                <p>
                    “We are very proud of the highly efficient operation we run at the auditor’s
                    office, allowing this surplus to accrue,” Kovack said. “Local governments can
                    now best serve their constituents with these additional moneys.”
                </p>
                <p>
                    The new record amount is 25 percent greater than the previous high. That 
                    previous record total came in 2009 when Auditor Kovack returned $4 million 
                    to local municipalities. Medina County had the lowest per parcel appraisal 
                    cost in the state that year. 
                </p>
                <p>
                    The $5 million amount was determined after accounting for the expenditures of
                    the 2013 Sexennial Reappraisal and estimating the level of money needed for 
                    the upcoming 2016 Triennial Reappraisal. The return of the $5 million still 
                    allows Kovack’s office ample resources for the immediate future, including 
                    sufficient resources to cover technology improvements.
                </p>
